# Define the complex numbers

z1 = 2 + 3j
z2 = 4 - 2j

# Perform the operations

addition = z1 + z2
subtraction = z1 - z2
multiplication = z1 * z2
division = z1 / z2

# Print the results

print("Addition of z1 and z2:", addition)
print("Subtraction of z2 from z1:", subtraction)
print("Multiplication of z1 and z2:", multiplication)
print("Division of z1 by z2:", division)

# Print the real and imaginary parts of each result

print("\nReal and Imaginary parts of each result:")
print("Addition - Real part:", addition.real, ", Imaginary part:", addition.imag)
print("Subtraction - Real part:", subtraction.real, ", Imaginary part:", subtraction.imag)
print("Multiplication - Real part:", multiplication.real, ", Imaginary part:", multiplication.imag)
print("Division - Real part:", division.real, ", Imaginary part:", division.imag)
